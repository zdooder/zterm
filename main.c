void print(const char* str) {
  unsigned char* d = (unsigned char*) (SCREEN_BASE + (*(CURSOR_Y)*40) + *(CURSOR_X));
  while(*str) {
    char c = *str++;
    switch(c) {
      case '\n': 
        *(CURSOR_X)=0;
        break;
      case '\r': 
        if ( ++(*CURSOR_Y) > 24 ) {
          scroll();
        }; 
        break;
      case '\x08':
        --*(CURSOR_X);
        print(" ");
        --*(CURSOR_X);
        break;
      default:
        *d++ = petscii_to_screen[c];
        if ( ++(*CURSOR_X) > 39 ) {
          *CURSOR_X=0;
          if ( ++(*CURSOR_Y) > 24 ) {
            scroll();
          }
        }
    }
  }
}

